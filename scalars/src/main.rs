fn main() {
    // integers
    let x: i32 = 42;
    let y: u64 = 100;
    println!("x = {}, y = {}", x, y);

    // floats
    let pi: f32 = 3.14;
    println!("pi = {}", pi);

    // booleans
    let is_snowing: bool = true;
    println!("Is snowing? {}", is_snowing);

    // chars
    let a: char = 'a';
    println!("a = {}", a);
}
